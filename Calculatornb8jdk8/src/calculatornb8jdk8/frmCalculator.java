    private double toDouble(String str){
        return Double.parseDouble(str);
    }
    private boolean calculate(){
        String[] strSplitInput = strInput.split(" ");
        List<String> strList = new ArrayList();
        for(String str: strSplitInput){
            strList.add(str);
        }
        double temp = 0;
        for(String operator: strOperators){
            for(int x=0;x<strList.size();x++){
                try{
                    if(strList.get(x).equals(operator)){
                        switch (operator){
                            case "/":
                                strList.remove(x);
                                x--;
                                temp = toDouble(strList.remove(x))/ toDouble(strList.get(x));
                                break;
                            case "x":
                                strList.remove(x);
                                x--;
                                temp = toDouble(strList.remove(x))* toDouble(strList.get(x));
                                break;

                        }
                        strList.set(x,String.valueOf(temp));
                    }else if(operator.equals("_") && (strList.get(x).equals("+")||strList.get(x).equals("-"))){
                        switch (strList.get(x)){
                            case "+":
                                strList.remove(x);
                                x--;
                                temp = toDouble(strList.remove(x))+ toDouble(strList.get(x));
                                break;
                            case "-":
                                strList.remove(x);
                                x--;
                                temp = toDouble(strList.remove(x))- toDouble(strList.get(x));
                                break;
                        }
                        strList.set(x,String.valueOf(temp));
                    }
                }catch(IndexOutOfBoundsException Ex){
                    System.out.println("IndexOutOfBoundErr: " + Ex.getLocalizedMessage());
                    return false;
                }catch (NumberFormatException Ex){
                    System.out.println("NumberFormatErr: " + Ex.getLocalizedMessage());
                    return false;
                }
            }
        }
        this.Calculated = Math.round(temp*100.0)/100.0;
        return true;
    }

