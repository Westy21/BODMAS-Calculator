    private String stripWhitespaces(String var){
        return var.replaceAll("\\s", "");
    }


    /**
     * Returns a string format of double (i) stripped-off of it's decimal points if
     * its not a decimal or just a string format if it's decimal.
     * */
    private String trimOutput(double i){
        if(i%1==0){
            return String.valueOf((int)i);
        }else{
            return String.valueOf(i);
        }
    }

